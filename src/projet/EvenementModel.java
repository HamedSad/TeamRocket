package projet;

public class CreationEvenement {
	
	String id;
	String titre;
	String dateEve;
	String createur;
	String lieu;
	String montant;
	String typeEve;
	String textEve;
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getTitre() {
		return titre;
	}
	public void setTitre(String titre) {
		this.titre = titre;
	}
	public String getDateEve() {
		return dateEve;
	}
	public void setDateEve(String dateEve) {
		this.dateEve = dateEve;
	}
	public String getCreateur() {
		return createur;
	}
	public void setCreateur(String createur) {
		this.createur = createur;
	}
	public String getLieu() {
		return lieu;
	}
	public void setLieu(String lieu) {
		this.lieu = lieu;
	}
	public String getMontant() {
		return montant;
	}
	public void setMontant(String montant) {
		this.montant = montant;
	}
	public String getTypeEve() {
		return typeEve;
	}
	public void setTypeEve(String typeEve) {
		this.typeEve = typeEve;
	}
	public String getTextEve() {
		return textEve;
	}
	public void setTextEve(String textEve) {
		this.textEve = textEve;
	}

	
	
}